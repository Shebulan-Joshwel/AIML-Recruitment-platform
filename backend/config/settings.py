# Read .env into a dict (no reliance on os.environ for DB)
_env: dict = {}
_env_path = BASE_DIR / ".env"
if _env_path.exists():
    with open(_env_path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                key, _, value = line.partition("=")
                _env[key.strip()] = value.strip()
for k, v in _env.items():
    os.environ.setdefault(k, v)

SECRET_KEY = _env.get("DJANGO_SECRET_KEY") or os.environ.get("DJANGO_SECRET_KEY", "change-me-in-production")

